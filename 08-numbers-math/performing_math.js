let num1 = 100;  // variable definition

console.log(" Basic Math ");
console.log(num1 + 25) //adition
console.log(num1 - 100) //subtraction
console.log(num1 * 100) // multiplication
console.log(num1 / 1500) //division

console.log(" Additional Math Operations ");
console.log(num1 % 1500) //modulo = celočíselný zbytek po dělení
console.log(++num1) //increment
console.log(--num1) //decrement

console.log(" Using Math Object ");
console.log(Math.PI); //Pi 
console.log(Math.sqrt(num1)); //square root - odmocnina

